}

/** Validate Reply **/
function validateForumReply( $PostData, $replyStr ){
    $postSlug = strtolower( preg_replace("/[^A-Za-z0-9\-]/", "", str_replace(' ', '-', $PostData->title) ));
    $postSlug = substr( preg_replace('/[-]+/', '-',  $postSlug), 0, 20 );
    if( $postSlug == $replyStr ){
        return true;
    } else {
        return false;
    }
}
