    // Get file type based on file extension
    function getFileType($filename)
    {
        if( !$filename ) {
            return null;
        }

        $nameArr= explode(".", $filename);
        $ext = $nameArr[count($nameArr)-1];

        if($ext == 'mp4') {
            return 'video';
        } elseif($ext == 'mp3') {
            return 'audio';
        } else {
            return 'image';
        }
    }

